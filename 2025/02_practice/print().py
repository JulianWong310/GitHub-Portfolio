# What can be use inside print()?
# 1) String
print("SUPER MARIO 3D WORLD ")
print('Plessie is pink.')
# Include quotation marks inside print()
# you can switch to single quotes
print("Louis said 'not fish again, I want cheesy pizza bun.'")
# Or use escape characters
print("My favourite book is \"I'm not scared, you're scared\"")

# 2)Numbers: integers or decimals
print(5786867484769)
print(0.5)

# 3) Multiple items separated by commas
print("My age is", 4282842552229)

# 4) Concatenation: use "+" to join strings
print("Ludwig"  +   "Plessie"  +   "Piranna plant")
# print(12  + "Ludwig"  )
# This is wrong because number and string can't be added together in print()

# 5) Math expression
print(298470*96/10+999-689)


